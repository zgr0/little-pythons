from tkinter import *
window = Tk()
window.geometry("250x400")
buttons=Frame(window)

text=StringVar()
entry= Entry(window,font=("digital-7",40),width=buttons.winfo_screenwidth(),bg="green",textvariable=text)
entry.pack()
buttons.pack()
def type(x):
    entry.insert(len(entry.get())+1,x)
def equal():
    try:
        
        solution=str(eval(entry.get()))
        text.set(solution)
    except SyntaxError:
        text.set("syntax error")
    except ZeroDivisionError:
        text.set("arithmetic error")

def clear():
    text.set("")
def backspacef():
    entry.delete(len(entry.get())-1,END)
